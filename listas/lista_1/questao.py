#Faça um programa que peça dois números e imprima a soma

#n1 é a variavel 1 numero, o n2 é a variavel para o 2 númer0
n1 = int(input("digite o primeiro número ")) #input é a função para que o usuario digite um valor, por padrão o input salva oq o user digitar como string 
n2 = int(input("digite o segundo número ")) 

soma = n1+n2  #soma é a variavel onde salva o resultado n1+n2

print("A soma dos dois números é:", soma) #print para mostrar o resultado 